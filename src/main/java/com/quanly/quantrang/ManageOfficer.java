    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String officer_rank = txtRank.getText();
        String phoneNumber = txtPhoneNumber.getText();

        if (validateFields()) {
            JOptionPane.showMessageDialog(null, "All fields are required!");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update officer set name=?, officer_rank=?, phoneNumber=? WHERE officer_pk=?");
                ps.setString(1, name);
                ps.setString(2, officer_rank);
                ps.setString(3, phoneNumber);
                ps.setInt(4, officerPk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Officer Updated Successfully!");
                setVisible(false);
                new ManageOfficer().setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

